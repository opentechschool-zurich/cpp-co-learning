#include <vector>
#include <iostream>
#include <algorithm>
#include <functional>
 
int main()
{
    std::vector<int> c = {1, 2, 3, 4, 5, 6, 7};
    int x = 5;
    c.erase(std::remove_if(c.begin(), c.end(), [x](int n) { return n < x; }), c.end());
    std::cout << "c: ";
    std::for_each(c.begin(), c.end(), [](int i){ std::cout << i << ' '; });
    std::cout << '\n';
 
    // the type of a closure cannot be named, but can be inferred with auto
    // since C++14, lambda could own default arguments
    auto func1 = [](int i = 6) { return i + 4; };
    std::cout << "func1: " << func1() << '\n';
 
    // like all callable objects, closures can be captured in std::function
    // (this may incur unnecessary overhead)
    std::function<int(int)> func2 = [](int i) { return i + 4; };
    std::cout << "func2: " << func2(32) << '\n';

	char b = 'b';
	std::function<bool(char)> checkChar = [&](char check) { b='a'; return (check == b); };
	// we cannot change value b within lambda with [=]
	//std::function<bool(char)> checkChar = [=](char check) { b='a'; return (check == b); };
	std::cout << "checkChar with 'b' :" << checkChar('b') << std::endl;
	std::cout << "checkChar with 'a' :" << checkChar('a') << std::endl;
}
